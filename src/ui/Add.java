    private void nActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nActionPerformed
        try {
            RecipeSummary rs=getSummary();
            ArrayList<String>ap=getPictures();
            ArrayList<RecipeIngredient>ab=getBahane();
            ArrayList<RecipeStep>as=getLangkah();
            String jenis=getJenis();
            int i=util.Work.getCountRecipe()+1;
            model.Recipe r=new model.Recipe(""+i, nama.getText(), ap, rs, ab, as);
            util.Work.addRecipe(jenis,r);
            this.setVisible(false);
        } catch (ParserConfigurationException | SAXException | IOException | TransformerException ex) {
            Logger.getLogger(Add.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_nActionPerformed


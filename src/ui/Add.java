    private RecipeSummary getSummary() {
        return new model.RecipeSummary(origin.getText(), prepare.getText(), waktu.getText(), porsi.getText(), kkal.getText(), desc.getText());
    }

    private ArrayList<String> getPictures() {
        ArrayList<String> a=new ArrayList<String>();
        for(javax.swing.JInternalFrame i:picList.getAllFrames()){
            ui.inter.Pict p=(ui.inter.Pict) i;
            a.add(p.getGbr());
        }return a;
    }

    private ArrayList<RecipeIngredient> getBahane() {
        ArrayList<RecipeIngredient>a=new ArrayList<RecipeIngredient>(),c=new ArrayList<RecipeIngredient>();
        for(javax.swing.JInternalFrame i:bahanList.getAllFrames()){
            ui.inter.Bahan b=(ui.inter.Bahan) i;
            a.add(b.getBahane());
        }for(int x=a.size()-1;x>=0;x--)c.add(a.get(x));
        return c;
    }

    private ArrayList<RecipeStep> getLangkah() {
        ArrayList<RecipeStep>a=new ArrayList<RecipeStep>(),c=new ArrayList<RecipeStep>();
        for(javax.swing.JInternalFrame i:stepList.getAllFrames()){
            ui.inter.Step s=(ui.inter.Step) i;
            a.add(s.getLangkah());
        }for(int x=a.size()-1;x>=0;x--)c.add(a.get(x));
        return c;
    }

    private String getJenis() {
        if(jRadioButton1.isSelected())return jRadioButton1.getText();
        if(jRadioButton2.isSelected())return jRadioButton2.getText();
        if(jRadioButton3.isSelected())return jRadioButton3.getText();
        return"";
    }
